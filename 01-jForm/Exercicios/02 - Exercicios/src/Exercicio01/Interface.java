    private void somaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_somaActionPerformed
        if (inputV1.getText().isEmpty() || inputV2.getText().isEmpty()) {
            resultadoCalculo.setText("0");
        } else {
            Calculadora calc = new Calculadora(Integer.parseInt(inputV1.getText()), Integer.parseInt(inputV2.getText()));
            resultadoCalculo.setText(calc.soma(Integer.parseInt(inputV1.getText()), Integer.parseInt(inputV2.getText())) + "");
        }
    }//GEN-LAST:event_somaActionPerformed

